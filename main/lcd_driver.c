// ── Low-level helpers ────────────────────────────────────────────────────────

static void lcd_cmd(uint8_t cmd) {
    gpio_set_level(PIN_DC, 0);
    spi_transaction_t t = { .length = 8, .tx_buffer = &cmd };
    spi_device_polling_transmit(s_spi, &t);
}

static void lcd_data_bytes(const uint8_t *data, int len) {
    if (len == 0) return;
    gpio_set_level(PIN_DC, 1);
    spi_transaction_t t = { .length = (size_t)len * 8, .tx_buffer = data };
    spi_device_polling_transmit(s_spi, &t);
}

// ── Public API ───────────────────────────────────────────────────────────────

esp_err_t lcd_init(void) {
    ESP_LOGI(TAG, "Initialising ILI9341 LCD");

    // GPIO for DC and RST
    gpio_config_t io = {
        .pin_bit_mask = (1ULL << PIN_DC) | (1ULL << PIN_RST),
        .mode         = GPIO_MODE_OUTPUT,
    };
    gpio_config(&io);

    // SPI bus
    spi_bus_config_t bus = {
        .mosi_io_num     = PIN_MOSI,
        .sclk_io_num     = PIN_CLK,
        .miso_io_num     = -1,
        .quadwp_io_num   = -1,
        .quadhd_io_num   = -1,
        .max_transfer_sz = 4096,
    };
    ESP_ERROR_CHECK(spi_bus_initialize(SPI2_HOST, &bus, SPI_DMA_CH_AUTO));

    // SPI device
    spi_device_interface_config_t dev = {
        .clock_speed_hz = 40 * 1000 * 1000,   // 40 MHz
        .mode           = 0,
        .spics_io_num   = PIN_CS,
        .queue_size     = 7,
    };
    ESP_ERROR_CHECK(spi_bus_add_device(SPI2_HOST, &dev, &s_spi));

    // Hardware reset
    gpio_set_level(PIN_RST, 0);
    vTaskDelay(pdMS_TO_TICKS(100));
    gpio_set_level(PIN_RST, 1);
    vTaskDelay(pdMS_TO_TICKS(100));

    // Initialisation sequence
    lcd_cmd(CMD_SWRESET);
    vTaskDelay(pdMS_TO_TICKS(150));
    lcd_cmd(CMD_SLPOUT);
    vTaskDelay(pdMS_TO_TICKS(500));

    // 16-bit RGB565 colour mode
    lcd_cmd(CMD_COLMOD);
    { uint8_t d = 0x55; lcd_data_bytes(&d, 1); }


    // Landscape: MV=1, MY=1, BGR=1 (0xA8) — MY+MX toggled for correct orientation
    lcd_cmd(CMD_MADCTL);
    { uint8_t d = 0xA8; lcd_data_bytes(&d, 1); }

    // Panel powers up inverted by default — send INVON for correct colours
    lcd_cmd(CMD_INVON);

    lcd_cmd(CMD_DISPON);
    vTaskDelay(pdMS_TO_TICKS(100));

    ESP_LOGI(TAG, "LCD ready");
    return ESP_OK;
}

void lcd_set_window(uint16_t x0, uint16_t y0, uint16_t x1, uint16_t y1) {
    uint8_t d[4];

    lcd_cmd(CMD_CASET);
    d[0] = x0 >> 8; d[1] = x0 & 0xFF; d[2] = x1 >> 8; d[3] = x1 & 0xFF;
    lcd_data_bytes(d, 4);

    lcd_cmd(CMD_PASET);
    d[0] = y0 >> 8; d[1] = y0 & 0xFF; d[2] = y1 >> 8; d[3] = y1 & 0xFF;
    lcd_data_bytes(d, 4);

    lcd_cmd(CMD_RAMWR);
}

// Fill a solid rectangle.
// Uses a line buffer so each row is sent in one SPI transaction (much faster
// than the per-pixel approach which would do w*h separate 2-byte transfers).
void lcd_fill_rect(int x, int y, int w, int h, uint16_t color) {
    if (x < 0 || x >= LCD_WIDTH  || y < 0 || y >= LCD_HEIGHT) return;
    if (x + w > LCD_WIDTH)  w = LCD_WIDTH  - x;
    if (y + h > LCD_HEIGHT) h = LCD_HEIGHT - y;
    if (w <= 0 || h <= 0) return;

    // RGB565 big-endian: high byte first, low byte second
    uint8_t hi = color >> 8;
    uint8_t lo = color & 0xFF;

    // Build one row worth of pixel data in the static buffer
    int row_bytes = w * 2;
    for (int i = 0; i < row_bytes; i += 2) {
        s_line_buf[i]     = hi;
        s_line_buf[i + 1] = lo;
    }

    lcd_set_window((uint16_t)x, (uint16_t)y,
                   (uint16_t)(x + w - 1), (uint16_t)(y + h - 1));
    gpio_set_level(PIN_DC, 1);
    spi_transaction_t t = { .length = (size_t)row_bytes * 8, .tx_buffer = s_line_buf };
    for (int row = 0; row < h; row++) {
        spi_device_polling_transmit(s_spi, &t);
    }
}

void lcd_fill_screen(uint16_t color) {
    lcd_fill_rect(0, 0, LCD_WIDTH, LCD_HEIGHT, color);
}

void lcd_draw_pixel(int x, int y, uint16_t color) {
    if (x < 0 || x >= LCD_WIDTH || y < 0 || y >= LCD_HEIGHT) return;
    lcd_set_window((uint16_t)x, (uint16_t)y, (uint16_t)x, (uint16_t)y);
    // RGB565 big-endian: high byte first
    uint8_t c[2] = { color >> 8, color & 0xFF };
    lcd_data_bytes(c, 2);
}

// Draw an unfilled rectangle border
void lcd_draw_rect_outline(int x, int y, int w, int h, uint16_t color) {
    // Draw as four filled 1-pixel-wide rects for efficiency
    lcd_fill_rect(x,         y,         w, 1, color); // top
    lcd_fill_rect(x,         y + h - 1, w, 1, color); // bottom
    lcd_fill_rect(x,         y,         1, h, color); // left
    lcd_fill_rect(x + w - 1, y,         1, h, color); // right
}

// Draw a single 8×8 character
static void lcd_draw_char(int x, int y, char c, uint16_t fg, uint16_t bg) {
    if (c < 32 || c > 127) c = 32;
    int idx = c - 32;

    // RGB565 big-endian: high byte first
    uint8_t hi_fg = fg >> 8, lo_fg = fg & 0xFF;
    uint8_t hi_bg = bg >> 8, lo_bg = bg & 0xFF;

    // Build whole character (8×8 pixels) in one buffer, send in one transaction
    // Buffer: 8 rows × 8 cols × 2 bytes = 128 bytes
    uint8_t buf[128];
    for (int row = 0; row < 8; row++) {
        uint8_t bits = (idx < 96) ? font8x8[idx][row] : 0;
        for (int col = 0; col < 8; col++) {
            int pos = (row * 8 + col) * 2;
            if (bits & (1 << col)) {
                buf[pos]     = hi_fg;
                buf[pos + 1] = lo_fg;
            } else {
                buf[pos]     = hi_bg;
                buf[pos + 1] = lo_bg;
            }
        }
    }
    lcd_set_window((uint16_t)x, (uint16_t)y,
                   (uint16_t)(x + 7), (uint16_t)(y + 7));
    lcd_data_bytes(buf, 128);
}

void lcd_draw_string(int x, int y, const char *str, uint16_t fg, uint16_t bg) {
    int cx = x;
    while (*str) {
        lcd_draw_char(cx, y, *str++, fg, bg);
        cx += 8;
    }
}
